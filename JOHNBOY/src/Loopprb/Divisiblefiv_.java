package Loopprb;

public class Divisiblefiv_ {
	  public static void main(String[] args) {
	        int i = 100;
	        int j = 500;

	       while (i <= j) {
	        	if (i % 5 == 0) {
	                System.out.println("Numbers divisible by 5 between 100 and 500:"+i);
	            }
	            i++;
	    }
	
}

}
